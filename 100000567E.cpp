#include <cstdio>

using namespace std;

int main()
{
    double l,reward;
    scanf("%lf",&l);
    if(l <= 100000){
        reward = l * 0.1;
    } else if(100000 < l && l <= 200000){
        reward = 100000 * 0.1 + (l - 100000) * 0.75;
    } else if(200000 < l && l <= 400000){
        reward = 100000 * 0.1 + 100000 * 0.75 + (l - 200000) * 0.05;
    } else if(400000 < l && l <= 600000){
        reward = 100000 * 0.1 + 100000 * 0.75 + 200000 * 0.05 + (1-400000) * 0.03;
    } else if(600000 < l && l <= 1000000){
        reward = 100000 * 0.1 + 100000 * 0.75 + 200000 * 0.05 + 200000 * 0.03 + (l - 600000) * 0.015;
    } else if(l > 1000000){
        reward = 100000 * 0.1 + 100000 * 0.75 + 200000 * 0.05 + 200000 * 0.03 + 400000 * 0.015 + (l - 1000000) * 0.01;
    }
    printf("%.2lf",reward);
    return 0;
}
